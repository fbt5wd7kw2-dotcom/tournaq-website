  function getLang() {
    var stored = localStorage.getItem('tq-lang');
    if (stored && SUPPORTED.indexOf(stored) !== -1) return stored;
    var nav = ((navigator.language || navigator.userLanguage || '').slice(0, 2)).toLowerCase();
    return SUPPORTED.indexOf(nav) !== -1 ? nav : DEFAULT_LANG;
  }

  function applyTranslations(t) {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] != null) el.textContent = t[key];
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (t[key] != null) el.innerHTML = t[key];
    });
    var titleKey = html.getAttribute('data-i18n-title');
    if (titleKey && t[titleKey]) document.title = t[titleKey];
  }

  function updateSwitcher(lang) {
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      var active = btn.getAttribute('data-lang') === lang;
      btn.classList.toggle('lang-btn--active', active);
      btn.setAttribute('aria-pressed', active ? 'true' : 'false');
    });
  }

  function setLang(lang) {
    var t = LOCALES[lang] || LOCALES[DEFAULT_LANG];
    localStorage.setItem('tq-lang', lang);
    html.lang = lang;
    applyTranslations(t);
    updateSwitcher(lang);
  }

  function buildSwitcher() {
    document.querySelectorAll('.lang-switcher').forEach(function (container) {
      container.innerHTML = '';
      SUPPORTED.forEach(function (l) {
        var btn = document.createElement('button');
        btn.className = 'lang-btn';
        btn.setAttribute('data-lang', l);
        btn.setAttribute('aria-pressed', 'false');
        btn.textContent = l.toUpperCase();
        btn.addEventListener('click', function () { setLang(l); });
        container.appendChild(btn);
      });
    });
  }

  buildSwitcher();
  setLang(getLang());
